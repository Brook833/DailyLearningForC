/*
右值引用可以绑定到右值的引用。
左值引用可以绑定到左值的引用。

右值引用可以通过std::move()，绑定左值。
*/

/*
右值引用必须绑定到右值的引用，通过&&获得。
右值引用只能绑定到一个将要销毁的对象上，因此可以自由的移动其资源。

左值引用，也就是“常规引用”，不能绑定到要转换的表达式、字面常量或返回右值的表达式。
右值引用恰好相反，可以绑定到这类表达式，但不能绑定到一个左值上。

返回左值的表达式包括:返回左值引用的函数及赋值，下标、解引用和前置递增递减运算符。
返回右值的包括:返回非引用类型的函数及算数、关系、位和后置递增递减运算符。

左值有持久的状态。
右值是短暂的。
*/